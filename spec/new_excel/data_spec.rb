    it "should be able to evaluate all the rows with the header" do
      @obj.get(with_header: true).should == [
        ["Date", "Time", "Open", "High", "Low", "Close", "Volume", "NumberOfTrades", "BidVolume", "AskVolume"],
        ["2019/03/01", "0:00:00", "114 16.75/32", "114 17/32", "114 16.75/32", "114 16.75/32", "653", "107", "524", "129"],
