      @obj.get(1).should == [
        "2019/03/01",
        "2019/03/01",
        "2019/03/01",
        "2019/03/01",
        "2019/03/01",
        "2019/03/01",
        "2019/03/01",
        "2019/03/01",
        "2019/03/01",
      ]
    end

